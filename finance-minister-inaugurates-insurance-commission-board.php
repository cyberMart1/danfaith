                        <h2 class="most_text"></h2>
                    <p class="bold">
                        The Minister of Finance and Coordinating Minister of the Economy, Wale Edun, on Tuesday in Abuja welcomed the board members of the National Insurance Commission to their responsibilities.                                              
                    </p>
                    <p>
                        President Bola Tinubu had earlier approved the appointment of the board members on April 19.
                        <br><br>
                        In a statement signed by the Special Adviser to the President (Publicity and Media), Ajuri Ngelale, the President approved the appointment of the following qualified Nigerians to the Board of NAICOM: Ms Halima Kyari as Chairperson, Mr Olusegun Omosehin as Commissioner for Insurance, Mr Olawoye Gam-Ikon as Deputy Commissioner (Technical Operations), Dr Usman Jimada, as Deputy Commissioner (Finance & Administration), Dr Miriam Kachikwu, Mr Adeniyi Fabikun and Mr. Umar Mohammed as Members respectively.
                        <br><br>
                        The President expects the new board of NAICOM to exercise utmost probity as it leads the commission in ensuring a safe, sound, and stable insurance sector. Their responsibilities include protecting policyholders, serving the public interest, and improving trust and confidence in the sector.
                    </p>
                    <p>
                        NAICOM was established in 1997 by the National Insurance Commission Act 1997 with responsibility for ensuring the effective administration, supervision, regulation, and control of the insurance business in Nigeria and the protection of insurance policyholders, beneficiaries, and third parties to insurance contracts.
                        <br><br>
                        While welcoming the board members, the Minister of Finance expressed great concern about the enormous responsibility that lies ahead.
                        <br><br>
                        However, he expressed confidence in their capacity to deliver on their mandate, considering their track records.
                    </p>
                    <p>
                        He emphasized the importance of diligent work to fulfil the renewed hope agenda of President Bola Tinubu’s administration.
                        <br><br>
                        In his address, Edun highlighted the significance of the insurance sector in stabilizing the economy. He emphasised the long-term saving potential provided by the sector and urged the board to utilise resources effectively for the benefit of economic growth. With a population of over 220m and a median age of 17, Nigeria has demographic dividends that the insurance sector should leverage.
                        <br><br>
                        “I extend my heartfelt congratulations to each of you on receiving your appointment letters from the Secretary to the Federal Government. As the President’s representative, I recognise the weight of this responsibility and the significance it holds. Your impressive track records, past experiences, and capacity assure me that you are well-prepared for the challenges ahead,” he said.
                        <br><br>
                        Board Chairperson, Ms Halima Kyari assured the minister that the members were ready to work as a team to deliver on their core mandates. She expressed gratitude for the trust placed in them and pledged loyalty to the team’s mission.
                        <br><br>
                        “As a united team, we are committed to upholding the trust you’ve placed in us to lead this Commission. Our loyalty will steadfastly align with the directives and objectives you entrust to us. On behalf of the entire team, I extend our heartfelt gratitude. Your confidence in us is deeply valued, and we assure you that it will not be misplaced,” she said.
                        <br><br>
                        The Permanent Secretary of the Ministry of Finance, Mrs. Lydia Jafiya, also addressed the board members emphasising their role in supporting the President’s agenda. She commended their confidence and urged them to fulfil their responsibilities diligently.
