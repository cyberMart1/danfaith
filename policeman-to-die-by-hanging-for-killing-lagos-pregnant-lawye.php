                 <h2 class="most_text">Policeman to die by hanging for killing Lagos pregnant lawyer</h2>
                 <p class="lorem_text">
                    It was justice at last for the family of Gbenga Raheem, yesterday, as a High Court, sitting in 
                     Tafa Balewa Square (TBS), Lagos, sentenced a dismissed Assistant Superintendent of Police (ASP),
                      Drambi Vandi, to death by hanging for killing Raheem’s pregnant wife, Omobolanle.
                 </p>
                 <p>Vandi at the Ajah under-bridge checkpoint on December 25, 2022, shot Omobolanle, a 41-year-old lawyer, to death in the presence of her husband, Gbenga.</p>
                 <p>Following the&nbsp;ensuing outcry, Lagos State government arraigned&nbsp;him on a count murder charge, in line with the provision of Section 223 of the Criminal Law of Lagos State, 2015.</p>
                 <p>The defendant pleaded not guilty to the charge of murder.</p>
                 <p>During the trial, the prosecution led by the Director of Public Prosecution (DPP), Dr. Babajide Matins called 11 witnesses, which include eight police officers and tendered 27 exhibits in evidence.</p>
                 <p>Only the defendant testified in his defence.</p>
                 <p>In his defence, Vandi told the court that the loud gunshot heard by the eyewitnesses was that of a firecracker.</p>
                 <p>Also, the defendant’s counsel, Mr. Adetokunbo Odutola, on February 20, filed a no-case submission application before the court. In the application, Vandi asked the court to dismiss the suit and discharge him, claiming that he has no case to answer.</p>
                 <p>In his arguments, the defence counsel told the court to note among other things that none of the eyewitnesses saw the defendant shoot and that the ballistic report of the gun allegedly fired expressly stated that the bullet cannot be linked with any of the firearms recovered from the police officers at the scene.</p>
                 <p>Odutola also urged the court to note the testimony of the pathologist who confirmed that the death of the deceased was as a result of a gunshot injury at the left axillary fold and the bullet moved from a posterior to anterior and the bullet pierced through the chest.</p>
                 <p>However, the court in its ruling on April 3, rejected the application and held that the defendant has a case to answer and paved the way for his trial.</p>
                 <p>The court also&nbsp;held that a banger cannot kill, and that he didn’t give further explanation or provide contrary expert evidence.</p>
                 <div>
                    <img src="uncategorized_news/late_omobolanle.jpg"><br>
                    <span><i>Late Omobolanle Raheem</i></span>
                </div>
                 <p>In her judgment, yesterday, the judge found Vandi guilty of murder as charged by the state government.</p>
                 <p>Justice Harrison held that&nbsp;the prosecution witnesses gave ‘overwhelming circumstantial evidence’ against him.</p>
                 <p>According to her, the prosecution established sufficient oral and documentary evidence linking the defendant to the crime.</p>
                 <p>The judge also held that the evidence of the deceased’s sister who had gone on to hold on to the defendant’s shirt after the shooting, as well as bystanders who pointed at him as the shooter were incontrovertible.</p>
                 <p>The court said the defendant did not provide cogent evidence to dispel the fact that he was seen to have fled into a mini bus, where he was dragged out and taken to the hospital, where the late Mrs. Raheem was rushed to, adding that he even changed into a mufti and hid under a staircase there.</p>
                 <p>“The pathologist said the deceased was fired at close range, and the armourer also testified for the state that two bullets were missing from his weapon afterwards.</p>
                 <p>“The ballistician’s report was found to be inconclusive because the defendant’s gun upon examination, had some defects as the bullet fragments retrieved from the vehicle couldn’t be matched with the gun. But, from the autopsy report, she was shot at close range,” the court said.</p>
                 <p>The court said the faults will not result in the gun firing itself and that it was the only weapon fired that day.</p>
                 <p>According to the judge, it is&nbsp;strange that the defendant who was the police team lead at the checkpoint was the only person at the scene that didn’t hear the loud gunshot heard by the other eyewitnesses.</p>
                 <p>The judge said: “The deceased was shot at close range and hit through the back windshield.</p>
                 <p>“The defendant was the only armed person to shoot from the right side as the other officer on the left wasn’t armed.</p>
                 <p>“None of the eye witnesses saw him actually shoot, but that would have been near impossible. The best a witness can do is to see him point the gun and to hear a gunshot.”</p>
                 <p>The court, therefore, found the defendant guilty on one count charge of murder and pronounced that he will be hanged in the neck till he dies.<br>
                    “You will be hanged by the neck till you are dead,” the court declared.<br>
