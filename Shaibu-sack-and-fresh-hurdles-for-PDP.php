                    <h2 class="most_text">Edo 2024: Shaibu's sack and fresh hurdles for PDP</h2>
                    <p class="bold">Edo State governor, Godwin Obaseki, may have succeeded in sacking his estranged deputy, Philip Shaibu. But the development has opened a new chapter in their war of attrition, with consequences for the ruling Peoples Democratic Party (PDP) in its quest to win the September 21 governorship election, MICHAEL EGBEJULE reports.</p>
                    <p>
                        The mutuallove lost between Edo State governor, Godwin Obaseki, and the immediatedeputy governor, Philip Shaibu, reached its feverish pitch on Monday, April 8, with the impeachment of Shaibu by the State House of Assembly.
                    </p>
                    <p>
                        Shaibu was impeached for alleged perjury and leaking of government documents. It is an open secret that the sour relationship between the two over Shaibu’s governorship ambition degenerated into irreconcilable differences that led to his impeachment. However, less than five hours after he was sacked, Omobayo Marvelous Godwins, was sworn-in as his replacement in what many observers believe has opened a fresh chapter in the political battle that has polarised members of the Peoples Democratic Party (PDP) and slowed down governance in the state.
                    </p>
                    <p>
                        Omobayo, the 37-year-old engineer and player in the oil and gas sector, contested the House of Representatives election for Akoko Edo Federal Constituency on February 25, last year, on the ticket of the Labour Party (LP), but lost to Mr Peter Akpatason of All Progressives Congress (APC).
                    </p>
                    <p>
                        While there is a pall of uncertainty on how the impeachment of Shaibu will affect the fortunes of the party in the September 21 governorship election, some observers believe that the development is not a credit to the PDP.
                    </p>
                    <p>
                        It was also believed that Obaseki, by his action especially the manner Shaibu was sacked, has de-marketed PDP. Some members of the party, sympathetic to Shaibu’s case, are said to be on their way to other parties especially, APC.
                    </p>
                    <p>
                        Credible sources confided in The Guardian that the next few weeks would witness exodus of some PDP members to APC if the national leadership of the party did not wade into the growing disquietedness over the sack of the deputy governor, who is also seen as a mobiliser.
                    </p>
                    <p>
                        The visit of Edo State APC chairman Emperor Jaret Tenebe to Shaibu, less than 48 hours after he was impeached, gave credence to the concerns of the party leaders.
                    </p>
                    <p>
                        Terebe, who described the impeachment as a sham, assured Shaibu that, “I can tell you that we will not allow you to walk alone we are with you in spirit. You are a strong man and I am here as the chairman of APC in Edo State, and I am also an Uzairue indigene where we both come from.
                    </p>
                    <p>
                        “We should be strong and I implore you to continue to pursue this matter in court and we will be with you all through and God is your strength. When one door is closed many more are opened and many are opened for you from today. That is what the detractors don’t know, be strong and we will match on together.”
                    </p>
                    <p>
                        Responding to Edo APC chairman comment, Shaibu who described him as his “chairman” said, “We have shared a lot of things both good and bad together. I am deeply humbled and happy that even this situation has brought us together as one that we are. You are one person that I have always admired, someone that I grew up with.
                    </p>
                    <p>
                        “The person that separated us has also united us and that is the beauty of what has happened. I want to assure you that as we move on, just like you said, this whole thing happened between Lent and Ramadan and the lessons of Lent and Ramadan are obvious. I am not ready to change my attitude, I am ready to be a friend and loyal friend and partner.
                    </p>
                    <p>
                        “I can assure you, my chairman, I call you my chairman because you are already my chairman and I think you are also one of the reasons they hurriedly did what they did because they know what it takes for you and me to be together and they thought they could use impeachment to get me to succumb but you know that I won’t succumb.”
                    </p>
                    <p>
                        But for supporters of the state governor, offloading Shaibu from his administration and getting another deputy from Akoko Edo in the same Edo North Senatorial Zone, amounted to killing two birds with one stone.
                    </p>
                    
                    <div class="about_img"><img src="politics/MR.-ASUE-IGHODALO.jpg"></div>
                    <p>
                        Recall that soon after Dr Asue Ighodalo, won the PDP governorship ticket, many observers noted that his emergence was aimed at addressing the marginalisation of Akoko-Edo axis in the scheme of things in the state. The area is allegedly being sidetracked from appointments, infrastructure and provision of social amenities by the Obaseki administration.
                    </p>
                    <p>
                        Shaibu, who hails from the same Edo North Senatorial District as former Governor Adams Oshiomhole, threw up a spirited internal challenge to Obaseki’s desire to show support to Edo Central Senatorial zone to succeed him. Using the instruments and visibility of his office, the former deputy governor became an internal opposition. Shaibu who described himself as a “Homeboy” derided his former principal’s choice of Ighodalo as an attempt to bring another outsider into the Edo Government House pretending to support a technocrat.
                    </p>
                    <p>
                        The impeachment process against him was an aftermath of some of the misgivings that trailed the PDP governorship primary. Governor Obaseki saw Shaibu’s excesses as an opportunity to rejig his administration and ward off complaints from the Akoko Edo Local Government Area.
                    </p>
                    <p>
                        It was also gathered that the governor allegedly ‘mobilised’ members of the House of Assembly to impeach Shaibu because since he has rejected the outcome of the PDP primary and is challenging it in court, it would be dangerous to allow him to enjoy the immunity and other privileges as deputy governor in pursuing the case.
                    </p>
                    <p>
                        So, removing him from office would strip him of all protocol and reduce his feasibility while the case lasts. Besides, with his impeachment, Obaseki’s supporters were relieved that their major internal enemy against their plan for the coming election had been removed from office.
                    </p>
                    <p>
                        On the other hand, however, some observers claim that the fall of the ‘Homeboy’, was karma at work. Those who believe that Shaibu’s ordeal was retributive justice at play are pointing to the events at the Edo State House of Assembly in 2010. They recalled how the ousted deputy governor, as the then Majority Leader of the State House of Assembly, orchestrated the suspension of Hon. Blessing Agbebaku, who is currently the Speaker of the State Assembly. Therefore, it was in conformity with the saying that “what goes around comes around” that the Agbebaku-led Assembly did not waste any time to impeach Shaibu when his matter was brought before the lawmakers.
                    </p>
                    <p>
                        However, reactions that are trailing Shaibu’s impeachment are beginning to heighten fears among PDP leaders about the fortunes of the party in the forthcoming governorship poll in the state. Indeed, some stakeholders believe that Shaibu’s sack, a few months before the governorship election, has created more enemies for the party.
                    </p>
                    <p>
                        Only on Saturday, a two-time PDP senator representing Edo South, Matthew Urhoghide, officially decamped to APC, as well as the former clerk of the House of Representatives and candidate for the Owan Federal Constituency in the 2023 election, Patrick Giwa.
                    </p>
                    <p>
                        Eight local government chairmen, including Frank Ilaboya of Owan West Local Government Area, also dumped PDP for APC.
                    </p>
                    <p>
                        Urhoghide has been announced as the Director General (DG) of the APC campaign organisation for the governorship election.
                    </p>
                    <div class="about_img"><img src="politics/Shaibu-1.jpg"></div>
                    <p>
                        The governorship candidate of the African Action Congress (AAC) DrUdohOberaifo, for instance, faulted the process of the impeachment as evidence of high-handedness.
                    </p>
                    <p>
                        He stated in Benin that regardless of the charges levelled against Shaibu, due process was not followed as he was not given a fair hearing.
                    </p>
                    <p>
                        “Of particular concern is the fact that the impeachment proceeded despite a pending case filed by the deputy governor at the Federal High Court. This legal action should have warranted a status quo, yet the impeachment was carried out regardless, casting doubts on the motives behind the hurried proceedings,” he declared.
                    </p>
                    <p>
                        Oberaifo, however, alleged an undisclosed relationship between LP and the ruling PDP in the state, saying that the new deputy governor being a card-carrying member of LP exposes that there is under-the-table collaboration.
                    </p>
                    <p>
                        He wondered why Omobayo, who is yet to resign his membership of LP, was sworn in as a deputy governor in a PDP-controlled state. The AAC candidate said: “This action is in clear violation of the Electoral Act 2022 and it underscores the connivance between the LP and PDP to consolidate power.”
                    </p>
                    <p>
                        A former PDP governorship aspirant Anselm Ojezua, also criticisedShaibu’s impeachment by the State House of Assembly.
                    </p>
                    <p>
                        Ojezua, who was among the 10 aggrieved PDP governorship aspirants during the party’s primary, noted that the offences Shaibu was alleged to have committed did not make sense, adding that it exposed the lawmakers as unserious people.
                    </p>
                    <p>
                        He stated: “That somebody complained of not being invited to an executive council meeting, which he is entitled to by law, is an impeachable offence. And that a retired judge who sat on the panel also admitted that it is an impeachable offence is worrisome.
                    </p>
                    <p>
                        “I am embarrassed and disappointed that the governor would allow such under his watch, knowing the role Shaibu played during his election as governor two times. I thought they would come up with allegations of corruption or scandalous conduct that would tend to embarrass the deputy governor’s office but that was not the case.
                    </p>
                    <p>
                        A constitutional lawyer, Librous Oshoma, said the sacking of Shaibu is a way of demarketing the 2024 governorship candidate of PDP and stated that the party has already become a house divided against itself.
                    </p>
                    <p>
                        He said regardless of the rift between Obaseki and his former deputy, the duo would have found ways to resolve it or managed it till the end of their tenure for the sake of the upcoming election.
                    </p>
                    <p>
                        He said, “No matter the offence of Philip Shaibu, impeaching him is a way of demarketing the PDP candidate because, for me, the PDP in Edo State is already a house divided against himself. No matter the misunderstanding, the governor should have allowed PDP to go one-on-one consistently with Shaibu to see how issues can be resolved for the sake of the election.”
                    </p>
                    <p>
                        Oshoma also attributed the crisis between Obaseki and Shaibu to the lack of clarity in the definition of duties between the governor and deputy governor roles. He said without such a definition, the deputy governor’s position remains vulnerable to the governor’s discretion, effectively rendering it subordinate.
                    </p>
                    <p>
                        He further called for a reexamination of Nigeria’s constitutional framework, with the aim of fostering a more equitable distribution of power and protecting the integrity of key governmental positions.
                        <br>
                    </p>
                     
