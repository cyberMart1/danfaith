                    <p>
                    In media and the political space, Nigerian women are also demanding attention for the 
                    #EndSARS protests. Aisha Yesufu and Ndi Kato, socio-political activists, have invaded 
                    Nigerian traditional media channels speaking forthrightly for the fight against police 
                    brutality. Journalist and Editor at CNN, Stephanie Busari, has also drawn international 
                    attention as well to ongoing evidence of police brutality during protests in the country.
                    </p>
                    <p>
