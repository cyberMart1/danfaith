                    <p class="bold">
                    Whenever revolutions are documented, women are erased. 
                     History is timelessly unkind to women and women’s contributions to politics and activism. 
                     When honor is due to be awarded, Black women’s accomplishments are at best, 
                     credited to the patriarchs, and at worst, labeled subversive. Experience has taught us, 
                     however, to give people their flowers while they can still smell them. Therefore, 
                     there is a need for us as Black women to tell our stories and document our history before 
                     they get the chance to be erased. In Sub-Saharan Africa currently, Nigerians have taken 
                     to the streets en masse protesting against police brutality by a notorious unit called SARS. 
                     Actively spearheading this movement is Nigerian women..
                    </p>
                    <p>
                        The Special Anti-robbery Squad (SARS) is a unit under the Nigerian police force created
                         in 1992 to fight crimes associated with armed robbery and kidnappings in the country. 
                         However, these so-called protectors are predators. In the past two decades, SARS 
                         officers have been responsible for innumerable extrajudicial killings, cases of human rights abuse, 
                         sexual harassment of women, and the brutalization of young Nigerians under the basis of ‘criminal 
                         profiling’. Originally a social media campaign, the #EndSARS protests took to the streets on Thursday, 
                         October 8th, in Lagos State. By Friday, the October 9th, #EndSARS was already trending globally on social 
                         media.
                    </p>
                    <p>
                        “In Sub-Saharan Africa currently, Nigerians have taken to the streets en masse 
                        protesting against police brutality by a notorious unit called SARS. Actively 
                        spearheading this movement is Nigerian women.”
                    </p>
                    <p>
                        From then on, it has become a series of nationwide protests and worldwide demonstrations 
                    of solidarity that have continued to attract international mainstream media attention. 
                    Notable celebrities like Viola Davis, Wizkid, Davido, Falz, Runtown, Burna Boy, Kanye West,
                     and Lil Baby have also shown support and there has been media coverage from CNN, 
                     Al Jazeera, and BBC as well. Angry and audacious, the Nigerian youth have clearly stated 
                     that unless their five demands are met, the protests will continue relentlessly.  
                    </p>
                    <p>
                    At the forefront of this revolutionary youth-led movement against police brutality in 
                    Nigeria is the Feminist Coalition – a group of young Nigerian feminists collectively 
                    mobilizing all facets of the global #EndSARS protests.  What initially began as a tweet 
                    from Feyikemi Abudu on October 9th, calling on Nigerians to donate 50,000 Naira ($130) 
                    towards the provision of food and water for protesters sleeping overnight in front of the 
                    Lagos Government House, has eventually morphed into unending donations rolling into 
                    millions of naira towards the #EndSARS movement. Food, water, legal fees, security, 
                    gas masks, medical supplies, and hospital bill payments for protesters have been adequately
                     covered from these donations over the past six days.
                    </p>
                    <p>
                    Every day of the nationwide protest this past week has started with Chef Obubu announcing 
                    the atypical protest menu. From shawarma to pizza, to jollof rice, to croissants and hot 
                    chocolate, the food team of the Feminist Coalition has ensured that protesters are well-fed
                     throughout each day of their march. These women have brilliantly deployed their phenomenal
                      culinary skills towards contributing to the sustenance of the protest momentum. 
                      Coordinating dispatch and logistics of resources is entrepreneur and travel consultant, 
                      Funmi Oyatogun who has dispatched and distributed food, security, mobile toilets, 
                      ambulances, and other resources for the protesters.
                    </p>
                    <p>
                    Ironically, these protests against police brutality are met with equal police brutality. 
                    Several peaceful protesters have been shot at; illegally arrested, beaten, detained; and 
                    some even killed by the police. Notwithstanding, legal aid was always on standby. 
                    Through the unrelenting efforts of Feyikemi Abudu and Modupe Odele, bail arrangements and 
                    hospital services for detainees and protesters who had been injured or shot were made 
                    available. Regardless of time or distance, these women were always ready to offer 
                    assistance where necessary, and have so far been able to release over 70 illegally 
                    detained protesters.
                    </p>
                    <p>
                    On the frontlines of the nationwide protest grounds mobilizing and motivating, Nigerian 
                    women were also present. Olorunrinu Oduala, a young Nigerian woman, was one of the few 
                    protesters who organized and slept in front of the Lagos Government House demanding an 
                    audience on the first day of the protest. Speaking boldly to the press, she stated that 
                    until the government took actionable steps towards meeting their demands, they (herself 
                    and the other protesters included) would not back down. Popular Twitter feminists Tife 
                    Soloye, Ozzy Etomi, Kiki Mordi, Ebele, Jola Ayeye, and Uloma Nwoke also joined the 
                    subsequent marches in Lagos. In the Diaspora as well there has been #ENDSARS movement 
                    building, with Fakrriyah Hashim and Karo Omu speaking in London demonstrations; Bola Adams 
                    and Ndali Ozegbe coordinating New York protests; and many more women worked tirelessly 
                    speaking, marching, demonstrating, and documenting these protests.
                    </p>
                    <p>
                    Likewise, online protesters are not left out either. There has been an outpour of monetary donations 
                    to sponsor cell phone airtime from all over the country. These donations have gone into supporting 
                    constant tweeting and ensuring that #EndSARS stays at the top of the daily social media trend table. 
                    Both online and offline, Nigerian feminists worked around the clock rallying and distributing resources 
                    to facilitate the movement. 
                    </p>
                    <p>
                    “It is truly fascinating to see how Nigerian feminists have covered all bases and invaded 
                    every possible nook and cranny of the online and offline #EndSARS protests.”
