                        <h2 class="most_text">Tinubu meets Chinese investors who built 4,000 metric tonnes a day Lithium processing plant </h2>
                    <p class="bold">
                        President Bola Tinubu has commended the inauguration of Nigeria’s largest lithium processing plant 
                        in Lafia, Nasarawa State, hailing it as a clear indication of the country’s attractiveness for 
                        foreign investment.
                    </p>
                    <p>
                     President Tinubu made the remarks on Friday in Abuja during a meeting with Governor 
                     Abdullahi Sule of Nasarawa State, who was accompanied by Mr. Hi Yongwei, Chairman of 
                     Avatar New Energy Materials Company Limited, and Mr. Zhenhua Pei, Chairman, Canmax 
                     Technologies.
                     <br><br>
                     Avatar, a Chinese firm, built the lithium processing plant which produces about 4,000 
                     metric tonnes daily while Canmax Technologies, a renowned Chinese firm responsible for 
                     over 30% of global battery material production, announced a new investment of $200 million
                      for another lithium processing plant in Nasarawa State.
                  </p>
                  <p>
                     Welcoming the inauguration of the plant and the announcement of new investment plans, 
                     President Tinubu urged the Chinese firms to prioritize environmental protection, community
                      engagement, and corporate social responsibility initiatives as integral parts of their 
                      operations.
                     <br><br>
                     ”There are other aspects of lithium that you are exploring in the country, especially in 
                     battery production. Nigeria is a huge market for solar panels. Africa is a major consumer
                      of solar technologies. I do not see why these panels and batteries cannot be produced 
                      here. The labour is cheaper. Our youths are vibrant and skilled. Our people are brilliant
                       and adapt to new technology. The economy is increasingly more vibrant, and Nigeria is 
                       dependable. We have the consumption capacity and a surplus of steady-handed citizens 
                       with gifted minds and an innate drive to work and produce.
                     <br><br>
                     ”You must not leave the community in ruins as you explore for our high-grade minerals. 
                     You must be concerned with cooperation and always care for the community.
                     <br><br>
                     ”We are caring partners. We want your investments to succeed so that you can expand 
                     further. Whenever you call on us, we will help you. You can, in mutually-beneficial 
                     ollaboration with us, dominate the solar panel market as part of a revolution in Africa 
                     and the West African sub-region.
                  </p>
                  <p>
                     ”You can always promote the interest of China and Nigeria as the best place in Africa to 
                     do business. We are preparing to produce in this country the solar technologies that the 
                     entire continent will use,” the President said.
                     <br><br>
                     President Tinubu also applauded Governor Sule and the Minister of Solid Minerals, 
                     Mr. Dele Alake, for their dedication to ensuring environmental protection and value 
                     addition by mining firms operating in the country.
                     <br><br>
                     ”The Governor of Nasarawa State and the Honourable Minister of Solid Minerals, Dele Alake,
                      also share in these congratulations.
                  </p>
                  <p>
                     ”It is a great joy to have you (the Chinese executives) in Nigeria. It is part of our 
                     campaign that Nigeria is open for business. We congratulate your courage in believing in 
                     us and the inauguration of the new factory.
                     <br><br>
                     ”I can assure you that we will continue to give you the necessary support and 
                     encouragement. This is an opportunity for you to help tell the rest of the investment 
                     world that Nigeria is solid and open for business,” the President said.
                     <br><br>
                     In his remarks, Governor Sule thanked the President for his unflinching support toward 
                     solid mineral development in the country and for sending Senate President Godswill Akpabio
                      to represent him at the inauguration.
                     <br><br>
                     ”The company that we just commissioned today in Nasarawa is a 500-million-dollar turnover
                      company. They are happy, and they have seen enough potential to mine lithium in the 
                      region for the next 15 to 20 years,’’ the Governor said.
                  </p>
                  <p>
                     In his remarks, Mr. Alake said the ministry has embarked on proactive measures to address
                      the risks posed by abandoned mines across the country, left behind by colonialists in the
                       1950s and 1960s.
                     <br><br>
                     ”We are in the process of putting in place remedial measures, converting some of them 
                     into constructive uses, like farming and irrigation.
                     <br><br>
                     ”To ensure that companies operating in this sector no longer abandon the mines after they
                      have finished operations, it is part of our requirements that for licencing fresh 
                      applicants, there must be concrete remediation plans that are viable and working before 
                      any application is approved for mining,” he said.
                     <br><br>
                     The Minister also said applicants seeking mining licences must demonstrate a commitment 
                     to enhancing local value across the entire mining value chain, aligning with the 
                     government’s agenda to promote indigenous participation and economic empowerment.
                     <br><br>
                     ”Your Excellency, in a nutshell, permit me to revel in the belief with conviction that 
                     under your leadership, with all the policy initiatives you have put in place, there is 
                     nowhere else for Nigeria to go but forward. Forward ever and backward never,” the Minister
                      said.
                     <br><br>
                     In separate remarks, the Chinese executives described their investments in Nigeria as 
                     worthwhile and pledged to adhere to mining regulations while fulfilling their corporate 
                     social responsibilities to host communities.
                  </p>
                    
