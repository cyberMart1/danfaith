                <h2 class="most_text">Why Nigeria wants to remove police roadblocks</h2>
                <article>
                    <p>
                        Nigeria's police chief Ibrahim Idris has ordered the immediate removal of all police roadblocks 
                        across the country.
                        <br><br>
                        Many Nigerians will welcome the news because these roadblocks are often little more than excuses to 
                        demand bribes from both ordinary travellers and those transporting goods.
                        <br><br>
                        The police statement, however, said the move was intended to reduce time wasted at roadblocks 
                        and ease business.
                        <br><br>
                        <strong>What happens at a roadblock?</strong>
                        <br>
                        Meeting a policeman on the road can be an exhausting experience.
                        <br><br>
                        The Ak47 slung casually across his shoulder does little to reassure you about your safety.                    
                    </p>
                    <p>
                        More than likely he - and it is invariably a man - would ask you to identify yourself and 
                        provide a driving licence.
                        <br><br>
                        Then he would ask to check the boot of your car. Next he would verify that you have 
                        everything required by law - a fire extinguisher in each car, a warning triangle (C-caution),
                        insurance, roadworthiness certificate and other documentation.
                        <br><br>
                        Any omission would lead to the choice of accompanying him to the police station, or an 
                        offer to "help you settle it" then and there, (pay a bribe to let you go).
                        <br><br>
                        Even if you are up to date on everything, that doesn't mean you can proceed with your 
                        journey.
                        <br><br>
                        The officer would subtly ask if you have "anything for the boys", or point out how hot the 
                        weather is (so he needs money for a drink).
                    </p>
                    <p>
                        This is not a rare occurrence, it is so common that Nigerian comedians often make fun of 
                        the police for soliciting bribes.
                        <br><br>
                        <b>What not to do at a checkpoint</b>
                        <br>
                        Receiving calls at a checkpoint can cause further delays.
                        <br><br>
                        Security forces generally take offence at people making calls in front of them.
                        <br><br>
                        Things could become worse if you try to film proceedings.
                        <br><br>
                        They do not take lightly to attempts to record them.
                        <br><br>
                        In 2013 police sergeant Chris Omeleze was sacked after a man filmed him trying to collect 
                        a bribe at the Lagos international airport. The victim posted the video on YouTube and it 
                        soon went viral.
                    </p>
                    <b>How do you know which is an official roadblock?</b>
                    <p>
                        There is no clear-cut way to distinguish between a real roadblock and a basic 
                        shake-down.
                        <br><br>
                        You will most likely see a barrel with the Nigeria police colours - blue, yellow and green - in the middle of the road.
                        <br><br>
                        At night a wobbling torch could be the only indicator of a police checkpoint ahead.
                    </p>
                    <p>
                        If you are unlucky it could also be armed robbers.
                        <br><br>
                        While some roadblocks are set up in order to catch criminals, others are merely a money-making scheme for corrupt police officers.
                        <br><br>
                        These often spring up at night and weekends.
                        <br><br>
                        The police chief has now ordered officers to wear uniforms with their name tags and service numbers visible. So a quick search on the police website could help you identify them and make a report.
                    </p>
                    <strong>How much is a bribe?</strong>
                    <p>
                        Nigeria's Bureau of Statistics (NBS) 2017 corruption survey said that 32% of adults 
                        who had come into contact with public officials had been asked for a bribe.
                        <br><br>
                        The NBS says a total of 82 million naira ($230,000; Â£170,000) was paid in bribes to 
                        public officials in Nigeria in the previous 12-month period. This equates to an 
                        average of one bribe per person per year.
                    </p>
                    <img src="uncategorized_news/truck-and-taxi.png" alt="Truck and Taxi images" width="50%" height="50%" >
                    <p>
                        A typical bribe from motorists would be 20 naira (five cents; four pence).
                        <br><br>
                        Worst hit are taxis and trucks transporting goods.
                        <br><br>
                        These groups are less likely to challenge authority and are often pressed for time, 
                        so they would rather pay a bribe.
                        <br><br>
                        A 2013 Transparency International global corruption barometer said that 92% of 
                        respondents in Nigeria felt the police were corrupt or extremely corrupt.
                    </p>
                    <strong>Will the roadblock ban succeed?</strong>
                    <p>
                        This is not the first attempt to rid Nigeria's streets of roadblocks.
                        <br><br>
                        In 2012, then police chief Mohammed Dahiru Abubakar made a similar order and more than
                         3,500 checkpoints were cleared out.
                        <br><br>
                        However, they have since found their way back.
                        <br><br>
                        This fresh order is another attempt to improve the image of the police.
                        <br><br>
                        According to the statement, special "x-squad" teams have been deployed throughout 
                        the country to arrest, investigate and discipline any police officer violating the 
                        directive.
                        <br><br>
                        Every police department must now seek the permission of the inspector general of 
                        police to mount roadblocks.
                        <br><br>
                        This is just one of the measures that Inspector General Idris has introduced to try 
                        and improve the generally negative public image of his force.
                        <br><br>
                        But it will take a long time, and concrete evidence of change, before most Nigerians 
                        agree with the slogan proudly posted on most police stations: "The police is your 
                        friend".
                    </p>
