                        <h2 class="most_text">SERAP survey: Police most corrupt institution in Nigeria</h2>
                    <p class="bold">
                    A new public survey released by Socio-Economic Rights and 
                     Accountability Project (SERAP) has adjudged the police as the most corrupt institution in 
                     Nigeria.
                    </p>
                    <p>
                        The SERAP report entitled “Nigeria: Corruption Perception Survey”, listed high levels 
                        of corruption in public institutions in the country since 2015.
                    <p>
                        Of the five major public institutions surveyed, the police emerged as the most corrupt,
                         with officials of the power sector identified as the second most corrupt in the country.
                    </p>
                    <p>
                        Other public institutions identified as corrupt were the judiciary, education and 
                        health ministries. According to the survey, the level of corruption has not changed in 
                        the last five years.
                    </p>
                    <p>
                        “A bribe is paid in 54 percent of interactions with the police. In fact, there is a 
                        63% probability that an average Nigerian would be asked to pay a bribe each time he or 
                        she interacted with the police. That is almost two out of three,” read a statement Kolawole Oluwadare, 
                        SERAP deputy director, issued on the report.
                    </p>
                    <p>
                        Akin Oyebode, chairman of the panel, which released the report, said the development of a people is 
                        almost “inversely proportional to the level of corruption”.
                    </p>
                    <p>“Nigeria is looked upon as a giant of Africa. Yet Nigeria could not conduct free, fair 
                        and credible elections,” 
                        he said.
                    <p>
                        “It is a smear on the image of Nigeria. If we do away with selective enforcement and 
                        condonation of corruption, we will build and live in a better society. Corruption is a 
                        refined form of stealing. The politicians are stealing our common patrimony.
                    </p>
                        The survey carried out between September and December, covered the police, judiciary, 
                        power, education and health sectors to assess the state of corruption in law 
                        enforcement and public service provision.
                    </p>
                    <p>
                        “From the analysis of the anti-corruption legal and institutional framework in Nigeria, 
                        the following cross-cutting issues emerged: there is lack of political goodwill to 
                        consistently enforce the different anti-corruption laws; inadequate funding for the 
                        various anti-corruption agencies; weak public support and/or ownership of anti-corruption 
                        initiatives; poor clarity of roles between various anti-corruption agencies; and public 
                        perceptions of politicisation of corruption arrests and prosecutions,” the report read.
                    </p>
                    <p>
                        “Bribery experiences were interrogated and recorded in the key sectors of education, 
                        health, the police, judiciary and power. Data analysis was conducted under five different 
                        and interrelated variables. There was a 63% probability that an average Nigerian would be 
                        asked to pay a bribe each time he/she interacted with the police.  The likelihood of bribery
                        in the power sector stood at 49%.  With the chances of encountering bribery at the judiciary,
                        education and health services standing at 27%, 25% and 20% respectively.”
                    </p>
                    <p>
                        The police were the most adversely ranked on this indicator. For every 100 police 
                        interactions reported by the respondents, there was a bribe paid in 54 interactions. 
                        The prevalence levels stood at 37% in the power sector and 18% in education,17.7% in the 
                        judiciary and 14% in the health sector.
                    </p>
                    <p>
                        Fifty-one percent of the individuals who paid bribes to the police and 35 percent to the 
                        power sector believed this was the only way to access the services sought from the 
                        institutions. The ranking of the education sector and the judiciary was less adverse with 
                        16 percent perceiving bribery as the main avenue of accessing services in the institutions,
                        and health services recording 13 percent
                    </p>
                    <p>
                        The police and judiciary had the largest proportion of total bribes paid at 33 percent and 
                        31 percent respectively. Bribes paid for education, power and health services accounted 
                        for 19 percent, 10.9 and 5 percent respectively of all bribes reported.
                    </p>
                    <p>
                        The average amount of bribe paid by the respondents was highest among those who paid to 
                        the judiciary at about Naira 108,000 (US$ 298). All the other institutions ranked lower on 
                        this variable with Naira 12,253 and 11,566 reportedly paid to the police and education 
                        sectors, and Naira 6,462 and 5,143 paid for health and power services respectively.
                    </p>
                    <p>
                        In 2017, the World Internal and Police Security Index rated Nigeria Police force as the worst in the world.
                    </p>
